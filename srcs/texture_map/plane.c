#include "includes/minirt.h"

void	plane_map(t_ray *ray)
{
	// check for y != 1
	ray->hit.uv.x = fmod(ray->hit.at.x, 1);
	ray->hit.uv.y = fmod(ray->hit.at.z, 1);
}